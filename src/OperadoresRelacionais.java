public class OperadoresRelacionais {

    public static void main(String[] args) {

        int numero1 = 1;
        int numero2 = 2;

        boolean simNao = numero1 == numero2;

        System.out.println("Número é igual a numero Dois? " + simNao);

        simNao = numero1 != numero2;

        System.out.println("Número é igual a numero Dois? " + simNao);
    }
}
